export function affectColor(a: Affect): string {
  switch (a) {
    case 'positive': return 'var(--cat-response)'
    case 'neutral': return 'var(--text-secondary)'
    case 'mixed': return 'var(--warning)'
    case 'negative': return 'var(--error)'
  }
}
export function affectLabel(a: Affect): string {
  return { positive: 'Pozitív', neutral: 'Semleges', mixed: 'Vegyes', negative: 'Nehéz' }[a]
}
